package com.java;

public class J05_Abstraction_vs_Encapsulation {
    public static void main(String[] args) {

        String abstraction = """
                Abstraction - Abstraction means hiding the internal implementation and only
                show useful information.
                """;

        String encapsulation = """
                Encapsulation - Encapsulation is hiding the data from outside interference.
                """;

        System.out.println(abstraction);
        System.out.println(encapsulation);
    }
}
